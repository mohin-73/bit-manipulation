#include <bits/stdc++.h>

using namespace std;

int updateIthBit(int num, int i, int val) {
    int mask = ~(1 << i);
    num = num & mask;
    num = num | (val << i);
    return num;
}

int main() {
    int n, i, v;
    cin >> n >> i >> v;
    cout << updateIthBit(n, i, v) << '\n';
    return 0;
}


#include <bits/stdc++.h>

using namespace std;

int clearBitsInRange(int num, int i, int j) {
    int a  = (~0) << (j + 1);
    int b = (1 << i) - 1;
    int mask = a | b;
    return num & mask;
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int n, i, j;
    cin >> n >> i >> j;
    cout << clearBitsInRange(n, i, j) << '\n';
    return 0;
}


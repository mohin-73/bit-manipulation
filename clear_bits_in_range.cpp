#include <bits/stdc++.h>

using namespace std;

int clearBitsInRange(int num, int i, int j) {
    int a = (~0) << (j + 1);
    int b = (1 << i) - 1;
    int mask = a | b;
    return num & mask;
}

int main() {
    int n, i, j;
    cin >> n >> i >> j;
    cout << "n (int) = " << n << '\n';
    cout << "n (bin) = " << bitset<32>(n) << '\n';
    cout << "i = " << i << ", j = " << j << '\n'; 
    int result = clearBitsInRange(n, i, j);
    cout << "Result (int) = " << result << '\n';
    cout << "Result (bin) = " << bitset<32>(result) << '\n';
    return 0;
}


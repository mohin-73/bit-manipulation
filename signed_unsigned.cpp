#include <bits/stdc++.h>

using namespace std;

void showBinary(const string &label, int num) {
    cout << setw(17) << left << label
         << " = " << setw(12) << num
         << " = " << bitset<32>(num) << '\n';
}

void showBinaryUnsigned(const string &label, int num) {
    unsigned int u = static_cast<unsigned int>(num);
    cout << setw(15) << left << label + " (unsigned)"
         << " = " << setw(12) << u
         << " = " << bitset<32>(u) << '\n';
}

void bitwiseOperation(const string &op, int x, int y, int result) {
    cout << "...................................................................\n";
    cout << op << "\n";
    cout << "...................................................................\n";
    showBinary("x", x);
    showBinary("y", y);
    showBinary("Result", result);
    showBinaryUnsigned("Result", result);
}

void bitWiseNOT(int x) {
    cout << "...................................................................\n";
    cout << "Bitwise NOT\n";
    cout << "...................................................................\n";
    showBinary("x", x);
    int res = ~x;
    showBinary("Result", res);
    showBinaryUnsigned("Result", res);
}

int main() {
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int x, y;
    cin >> x >> y;
    bitwiseOperation("Bitwise AND (x & y)", x, y, x & y);
    bitwiseOperation("Bitwise OR  (x | y)", x, y, x | y);
    bitwiseOperation("Bitwise XOR (x ^ y)", x, y, x ^ y);
    bitWiseNOT(x);
    bitWiseNOT(y);
    cout << "...................................................................\n";
    return 0;
}

